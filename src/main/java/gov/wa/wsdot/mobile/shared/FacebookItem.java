package gov.wa.wsdot.mobile.shared;

public class FacebookItem {
	private String message;
	private String htmlFormattedMessage;
	private String createdAt;
	private String id;
	private String link;
	private String type; 
	
	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
	public String getCreatedAt() {
		return createdAt;
	}
	
	public void setCreatedAt(String createdAt) {
		this.createdAt = createdAt;
	}

	public String getHtmlFormattedMessage() {
		return htmlFormattedMessage;
	}

	public void setHtmlFormattedMessage(String htmlFormattedMessage) {
		this.htmlFormattedMessage = htmlFormattedMessage;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
}